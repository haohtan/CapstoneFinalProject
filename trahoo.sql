DROP TABLE IF EXISTS profile CASCADE;
DROP TABLE IF EXISTS relationship CASCADE;
DROP TABLE IF EXISTS tag CASCADE;
DROP TABLE IF EXISTS search CASCADE;
DROP TABLE IF EXISTS favorite CASCADE;
DROP TABLE IF EXISTS comment CASCADE;
DROP TABLE IF EXISTS post CASCADE;
DROP TABLE IF EXISTS plan CASCADE;
DROP TABLE IF EXISTS days CASCADE;
DROP TABLE IF EXISTS partner CASCADE;


DROP TABLE IF EXISTS profile_post_comment CASCADE;
DROP TABLE IF EXISTS tag_post CASCADE;


CREATE TABLE profile (
	userID INT(6) NOT NULL AUTO_INCREMENT,
	email VARCHAR(50) NOT NULL,
	joinDate DATE NOT NULL,
	firstN VARCHAR(50),
	middleN VARCHAR(50),
	lastN VARCHAR(50),
	fullN VARCHAR(50),
	gender INT(1),
	dob DATE,
	sign TEXT,
	phoneNum CHAR(10),
	avatar_url VARCHAR(2083) NOT NULL DEFAULT 'https://cgi.luddy.indiana.edu/~team41/team-41/imgs/default-avatar.png',
	backgroundIMG VARCHAR(2083) NOT NULL DEFAULT 'https://cgi.luddy.indiana.edu/~team41/team-41/imgs/default_backgroundpg.jpg',
	verification VARCHAR(2083) NOT NULL DEFAULT '',
	verificationStatus INT DEFAULT 0,
	privacySetting INT DEFAULT 0,
	PRIMARY KEY (userID)
) ENGINE=INNODB;


CREATE TABLE relationship (
	relationshipID INT(6) NOT NULL AUTO_INCREMENT,
	userAID INT(6),
	userBID INT(6),
	status INT(1),
	viewed BIT DEFAULT 0,
	PRIMARY KEY (relationshipID),
	FOREIGN KEY (userAID) REFERENCES profile(userID),
   	FOREIGN KEY (userBID) REFERENCES profile(userID)
) ENGINE=INNODB;


CREATE TABLE tag(
    tagID INT(6) NOT NULL,
    content VARCHAR (250) NOT NULL,
    type INT(1) NOT NULL,
    PRIMARY KEY (tagID)
) ENGINE=INNODB;


CREATE TABLE search(
    searchID INT(6) NOT NULL AUTO_INCREMENT,
    search_content VARCHAR(250),
    tagID INT(6),
    userID INT(6) NOT NULL,
    searchedID INT(6),
    PRIMARY KEY (searchID),
    FOREIGN KEY (tagID) REFERENCES tag(tagID),
    FOREIGN KEY (userID) REFERENCES profile(userID),
    FOREIGN KEY (searchedID) REFERENCES profile(userID)
)ENGINE=INNODB;


CREATE TABLE favorite(
    favoriteID INT(6) NOT NULL AUTO_INCREMENT,
    category VARCHAR(7) NOT NULL,
    tagID INT(6),
    userID INT(6) NOT NULL,
    postID INT(6),
    PRIMARY KEY (favoriteID),
    FOREIGN KEY (tagID) REFERENCES tag(tagID),
    FOREIGN KEY (userID) REFERENCES profile(userID),
    FOREIGN KEY (postID) REFERENCES post(postID)
)ENGINE=INNODB;

CREATE TABLE comment(
    commentID INT(6) NOT NULL AUTO_INCREMENT,
    datetime Datetime,
    content TEXT,
    userID INT(6) NOT NULL,
    postID INT(6) NOT NULL,
    FOREIGN KEY (userID) REFERENCES profile(userID),
    FOREIGN KEY (postID) REFERENCES post(postID),
    PRIMARY KEY (commentID)
)ENGINE=INNODB;

CREATE TABLE post ( 
    postID INT(6) NOT NULL AUTO_INCREMENT, 
    title TEXT NOT NULL, 
    datetime Datetime,
    b_discription LONGTEXT NOT NULL, 
    content LONGTEXT NOT NULL, 
    costPerDay DECIMAL(6,2) NOT NULL, 
    image VARCHAR(500), 
    video VARCHAR(500), 
    clickNum INT(6),
	userID INT(6) NOT NULL,
    FOREIGN KEY (userID) REFERENCES profile(userID),
    PRIMARY KEY (postID) 
)ENGINE=INNODB;

CREATE TABLE plan ( 
    planID INT(6) NOT NULL AUTO_INCREMENT, 
    plan_title varchar(255) NOT NULL, 
    start_date DATE NOT NULL, 
    end_date DATE NOT NULL, 
    destination varchar(255), 
    estimate_cost INT(4), 
    average_cost INT(4), 
    userID INT(6),
    day_content LONGTEXT,
    PRIMARY KEY (planID), 
    FOREIGN KEY (userID) REFERENCES profile (userID) 
)ENGINE=INNODB;

CREATE TABLE days( 
    day INT(2) NOT NULL,	
    target_places VARCHAR(250), 
    daily_cost INT(4), 
    planID INT(6) NOT NULL,
    tagID INT(6) NOT NULL,
    FOREIGN KEY (planID) REFERENCES plan (planID), 
    FOREIGN KEY (tagID) REFERENCES tag (tagID) 										
) ENGINE=INNODB;
 
CREATE TABLE partner(
 	matchID INT(6) NOT NULL AUTO_INCREMENT, 
    num_people VARCHAR(20) NOT NULL, 
    gender VARCHAR(20) NOT NULL, 
    age VARCHAR(20) NOT NULL,
    planID INT(6) NOT NULL,
    Estimated_price varchar(20),
    FOREIGN KEY (planID) REFERENCES plan (planID) 
) ENGINE=INNODB;



CREATE TABLE tag_post(
    tagID INT(6) NOT NULL,
    postID INT(6) NOT NULL,
    FOREIGN KEY (tagID) REFERENCES tag(tagID),
    FOREIGN KEY (postID) REFERENCES post(postID)
) ENGINE=INNODB;





